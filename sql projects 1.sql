-- -------------------------------------------------------------------------------------------
-- ANALYSIS QUESTIONS
-- ----------------------------------------------------------------------------------------------
-- 1.CUSTOMER INSIGHTS
-- -------------------------------------------------------------------------------------------------

-- UNIQUE CUSTOMERS
SELECT COUNT(DISTINCT CUST_ID) AS UNIQUE_CUSTOMERS FROM ORDERS;

-- WHICH CUSTOMERS HAVE PLACED HIGHEST NO.OF ORDERS
SELECT C.CUST_ID,C.CUST_NAME,COUNT(O.ORD_ID) AS TOTAL_ORDERS
FROM CUSTOMERS C 
JOIN ORDERS O 
ON C.CUST_ID = O.CUST_ID
GROUP BY C.CUST_ID
ORDER BY TOTAL_ORDERS DESC
LIMIT 10;

-- TOTAL AND AVG PURCHASE VALUE PER CUSTOMER
SELECT O.CUST_ID, C.CUST_NAME, SUM(OD.TOTAL_PRICE) AS TOTAL_PURCHASE, AVG(OD.TOTAL_PRICE) AS AVG_PURCHASE
FROM ORDERS O
JOIN ORDER_DETAILS OD
ON O.ORD_ID = OD.ORD_ID
JOIN CUSTOMERS C
ON O.CUST_ID = C.CUST_ID
GROUP BY O.CUST_ID;

-- TOP 5 CUSTOMERS BY TOTAL PURCHASE AMOUNT
SELECT O.CUST_ID,C.CUST_NAME,SUM(OD.TOTAL_PRICE) AS TOTAL_PURCHASE
FROM ORDERS O
JOIN ORDER_DETAILS OD
ON O.ORD_ID = OD.ORD_ID
JOIN CUSTOMERS C 
ON O.CUST_ID = C.CUST_ID
GROUP BY O.CUST_ID
ORDER BY TOTAL_PURCHASE DESC
LIMIT 5;

-- -------------------------------------------------------------------------------------------------------
-- 2.PRODUCT PERFORMANCE
-- ------------------------------------------------------------------------------------------------------
-- HOW MANY PRODUCTS EXIST IN EACH CATEGORY
SELECT C.CAT_ID,C.CAT_NAME,COUNT(P.PROD_ID) AS TOTAL_PRODUCTS
FROM CATEGORIES C
LEFT JOIN PRODUCTS P
ON C.CAT_ID = P.CAT_ID
GROUP BY C.CAT_ID;

-- AVERAGE PRICE OF PRODUCTS BY CATEGORY
SELECT C.CAT_ID,C.CAT_NAME,AVG(P.PRICE) AS AVG_PRICE
FROM CATEGORIES C
LEFT JOIN PRODUCTS P
ON C.CAT_ID = P.CAT_ID
GROUP BY C.CAT_ID;

-- PRODUCTS HAVE THE HIGHEST TOTAL SALES (BY QUANTITY)
SELECT P.PROD_NAME, SUM(QUANTITY) AS HIGH_SALES
FROM ORDER_DETAILS O
LEFT JOIN PRODUCTS P
ON O.PROD_ID = P.PROD_ID
GROUP BY PROD_NAME
ORDER BY HIGH_SALES DESC;

-- TOTAL REVENUE GENERATED BY EACH PRODUCT
SELECT P.PROD_NAME, SUM(O.TOTAL_PRICE) AS TOTAL_REVENUE 
FROM ORDER_DETAILS O
LEFT JOIN PRODUCTS P
ON O.PROD_ID = P.PROD_ID
GROUP BY PROD_NAME;

-- PRODUCT SALES VARY BY CATEGORY AND SUPPLIER
SELECT C.CAT_NAME,S.SUP_NAME, SUM(OD.QUANTITY) AS SALES -- SUM(OD.TOTAL_PRICE) AS REVENUE
FROM  ORDER_DETAILS OD
JOIN PRODUCTS P 
ON OD.PROD_ID = P.PROD_ID
JOIN CATEGORIES C
ON P.CAT_ID = C.CAT_ID
JOIN SUPPLIER S
ON P.SUP_ID = S.SUP_ID 
GROUP BY C.CAT_NAME,S.SUP_NAME
ORDER BY C.CAT_NAME,SALES DESC;

-- --------------------------------------------------------------------
-- 3.SALES AND ORDER TRENDS
-- ---------------------------------------------------------------------
-- ORDERS HAVE BEEN PLACED IN TOTAL
SELECT COUNT(*) AS TOTAL_ORDERS
FROM ORDERS;

-- AVERAGE VALUE PER ORDER
SELECT SUM(TOTAL_PRICE)/COUNT(DISTINCT ORD_ID) AS AVG_ORDER_VALUE
FROM ORDER_DETAILS;

-- WHICH DATES WERE THE MOST ORDERS PLACED
SELECT ORDER_DATE, COUNT(ORD_ID) AS ORDERS_COUNT
FROM ORDERS
GROUP BY ORDER_DATE
ORDER BY ORDERS_COUNT DESC
LIMIT 10;

-- MONTHLY TRENDS IN ORDER VOLUME AND REVENUE
SELECT EXTRACT(MONTH FROM STR_TO_DATE(O.ORDER_DATE, '%m/%d/%Y')) AS ORDER_MONTH, 
COUNT(DISTINCT O.ORD_ID) AS ORDER_VOLUME, SUM(OD.TOTAL_PRICE) AS TOTAL_REVENUE
FROM ORDERS O 
JOIN ORDER_DETAILS OD
ON O.ORD_ID = OD.ORD_ID
GROUP BY ORDER_MONTH
ORDER BY ORDER_MONTH;

-- ORDER PATTERNS VARY ACROSS WEEKDAYS AND WEEKENDS
SELECT 
CASE 
   WHEN DAYOFWEEK(STR_TO_DATE(O.ORDER_DATE, '%m/%d/%Y')) IN (1,7)
   THEN 'WEEKENDS'
   ELSE 'WEEKDAYS'
   END AS DAY_TYPE ,
COUNT(DISTINCT O.ORD_ID) AS ORDER_VOLUME,
SUM(OD.TOTAL_PRICE) AS REVENUE,
SUM(OD.TOTAL_PRICE) / COUNT(DISTINCT O.ORD_ID) AS AVG_ORDER_VALUE
FROM ORDERS O
JOIN ORDER_DETAILS OD
ON O.ORD_ID = OD.ORD_ID
GROUP BY DAY_TYPE;

