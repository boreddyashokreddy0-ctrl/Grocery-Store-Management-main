SELECT S.SUP_ID,S.SUP_NAME, COUNT(P.PROD_ID) AS PRODUCTS
FROM SUPPLIER S
JOIN PRODUCTS P
ON S.SUP_ID = P.SUP_ID
GROUP BY S.SUP_ID
