-- AVERAGE PRICE OF PRODUCTS FROM EACH SUPPLIER?
SELECT S.SUP_ID,S.SUP_NAME,AVG(P.PRICE)
FROM SUPPLIER S
JOIN PRODUCTS P
ON S.SUP_ID = P.SUP_ID
GROUP BY S.SUP_ID;

-- SUPPLIERS CONTRIBUTE THE MOST TO TOTAL PRODUCT SALES (BY REVENUE)
SELECT S.SUP_ID,S.SUP_NAME,SUM(OD.TOTAL_PRICE) AS REVENUE
FROM SUPPLIER S
JOIN PRODUCTS P
ON S.SUP_ID = P.SUP_ID
JOIN ORDER_DETAILS OD
ON P.PROD_ID = OD.PROD_ID
GROUP BY SUP_ID
ORDER BY REVENUE DESC
LIMIT 1;

-- ----------------------------------------------------------------------------------------------
-- 5.EMPLOYEE PERFORMANCE
-- ----------------------------------------------------------------------------------------------
-- HOW MANY EMPLOYEES HAVE PROCESSED ORDERS?
SELECT COUNT(DISTINCT EMP_ID) AS TOTAL_EMPLOYEES_PROCESS_ORDERS
FROM ORDERS;

-- WHICH EMPLOYEES HAVE HANDLED THE MOST ORDERS?
SELECT E.EMP_ID,E.EMP_NAME,COUNT(DISTINCT O.ORD_ID) AS ORDERS
FROM EMPLOYEES E
JOIN ORDERS O 
ON O.EMP_ID = E.EMP_ID
GROUP BY E.EMP_ID
ORDER BY ORDERS DESC
LIMIT 5;

-- WHAT IS THE TOTAL SALES VALUE PROCESSED BY EACH EMPLOYEE?
SELECT E.EMP_ID,E.EMP_NAME,SUM(OD.TOTAL_PRICE) AS TOTAL_SALES
FROM EMPLOYEES E
JOIN ORDERS O 
ON O.EMP_ID = E.EMP_ID
JOIN ORDER_DETAILS OD
ON O.ORD_ID = OD.ORD_ID
GROUP BY E.EMP_ID
ORDER BY TOTAL_SALES DESC;

-- WHAT IS THE AVERAGE ORDER VALUE HANDLED PER EMPLOYEE?
SELECT E.EMP_ID,E.EMP_NAME,SUM(OD.TOTAL_PRICE)/COUNT(DISTINCT O.ORD_ID) AS AVG_ORDER_VALUE
FROM EMPLOYEES E
JOIN ORDERS O 
ON O.EMP_ID = E.EMP_ID
JOIN ORDER_DETAILS OD
ON O.ORD_ID = OD.ORD_ID
GROUP BY E.EMP_ID;

-- -----------------------------------------------------------------------------------------------------------------------------
-- 6.ORDER DETAILS DEEP DIVE
-- -----------------------------------------------------------------------------------------------------------------------------
-- THE RELATIONSHIP BETWEEN QUANTITY ORDERED AND TOTAL PRICE
SELECT QUANTITY, AVG(TOTAL_PRICE) AS AVG_PRICE,COUNT(ORD_ID) AS ORDER_COUNT
FROM ORDER_DETAILS
GROUP BY QUANTITY
ORDER BY QUANTITY;

-- THE AVERAGE QUANTITY ORDERED PER PRODUCT
SELECT P.PROD_ID,P.PROD_NAME, AVG(OD.QUANTITY) AS AVG_QUANTITY
FROM PRODUCTS P
JOIN ORDER_DETAILS OD
ON OD.PROD_ID = P.PROD_ID
GROUP BY P.PROD_ID,P.PROD_NAME
ORDER BY P.PROD_ID,P.PROD_NAME;

-- THE UNIT PRICE VARY ACROSS PRODUCTS AND ORDERS
SELECT P.PROD_ID,P.PROD_NAME,COUNT(DISTINCT O.ORD_ID) AS ORDERS,P.PRICE,AVG(OD.EACH_PRICE) AS SELLING_PRICE
FROM PRODUCTS P
JOIN ORDER_DETAILS OD 
ON OD.PROD_ID = P.PROD_ID
JOIN ORDERS O 
ON O.ORD_ID = OD.ORD_ID
GROUP BY P.PROD_ID , P.PROD_NAME , P.PRICE;
