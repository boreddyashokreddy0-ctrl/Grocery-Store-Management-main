-- 4.SUPPLIER CONTRIBUTION
-- ----------------------------------------------------------------------------------------
-- NO.OF SUPPLIERS ARE IN DATABASE
SELECT COUNT(*) AS NO_OF_SUPPLIERS FROM  SUPPLIER;

